# code → action のマップ化
ACTION_MAP = dict(PATTERNS)


def find_action(codes):
    """codes は ['INVALID_DATE', 'INVALID_TIME'] のような複数候補。最初にマッチしたものを返す。"""
    for c in codes:
        if c in ACTION_MAP:
            return ACTION_MAP[c]
    return None


# 該当コード行から code を抽出する正規表現
# 例: | 該当コード | `ACTIVATE_FAILED`<br>`ALREADY_PAID` |
# 例: | コード     | `EMPTY_BODY` |
CODE_LINE_RE = re.compile(r'^\| (?:該当)?コード\s*\| (.+?)\s*\|', re.MULTILINE)
INNER_CODE_RE = re.compile(r'`([A-Z][A-Z0-9_]*)`')

# 各 ### Exxxx 詳細ブロック
SECTION_RE = re.compile(
    r'^(### E\d{4}\n\n)'                                # header
    r'(\| 項目 \| 内容 \|\n\|---\|---\|\n'                # table header
    r'(?:\|.*?\|\n)+?'                                  # rows
    r')'
    r'(?=\n### |\n## |\Z)',                             # next section or EOF
    re.MULTILINE
)


def enrich(content):
    def repl(m):
        header = m.group(1)
        table  = m.group(2)
        # 既存「対処方法」行を一旦剥がす (冪等)
        table = re.sub(r'^\| 対処方法 \|.*\n', '', table, flags=re.MULTILINE)
        # コード抽出
        codes = []
        cm = CODE_LINE_RE.search(table)
        if cm:
            codes = INNER_CODE_RE.findall(cm.group(1))
        action = find_action(codes)
        if action:
            # 確認事項行の直前に対処方法を挿入
            new_row = '| 対処方法 | ' + action + ' |\n'
            # 末尾の確認事項行を見つけて、その前に挿入
            kakunin_re = re.compile(r'^(\| 確認事項 \|.*\n)', re.MULTILINE)
            if kakunin_re.search(table):
                table = kakunin_re.sub(new_row + r'\1', table, count=1)
            else:
                # 確認事項行が無いなら末尾に追加
                table = table.rstrip('\n') + '\n' + new_row
        return header + table

    return SECTION_RE.sub(repl, content)


def main():
    if not os.path.exists(CATALOG):
        print('Not found: ' + CATALOG, file=sys.stderr)
        sys.exit(1)
    with open(CATALOG, 'r', encoding='utf-8') as f:
        content = f.read()

    new_content = enrich(content)

    # 統計
    n_actions = new_content.count('| 対処方法 |')
    n_sections = len(re.findall(r'^### E\d{4}', new_content, flags=re.MULTILINE))
    print('Sections: %d, Actions injected: %d' % (n_sections, n_actions))

    if new_content == content:
        print('No changes (already enriched)')
        return

    with open(CATALOG, 'w', encoding='utf-8') as f:
        f.write(new_content)
    print('Updated: ' + CATALOG)


if __name__ == '__main__':
    main()
